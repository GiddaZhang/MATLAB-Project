    10.125 10.356 10.565 10.822 11.292 11.741 12.284 12.741 13.269 13.758 ...
    14.315 14.939 15.432];
    
    begin_idx = round(start(idx)*f_s);
    end_idx = round(start(idx+1)*f_s);
    
    y = music(begin_idx:end_idx);
end